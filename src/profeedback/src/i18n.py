def _norm_lang(code: str) -> str:
    code = code.replace("-", "_")
    prefix = code[:2].lower()
    upper = code.upper()
    if prefix == "zh":
        return "zh_TW" if any(token in upper for token in ("TW", "HK", "MO")) else "zh_CN"
    if prefix == "pt":
        return "pt_BR" if "BR" in upper else "pt_PT"
    if code.lower().startswith("jbo"):
        return "jbo"
    return code.split("_")[0].lower()


@lru_cache(maxsize=1)
def current_lang() -> str:
    try:
        import anki.lang

        code = (
            getattr(anki.lang, "current_lang", None)
            or getattr(anki.lang, "currentLang", None)
            or ""
        )
        key = _norm_lang(code)
        return key if key in _FLAG_STRINGS else "en"
    except Exception:
        return "en"


def flag_label(flag: FlagKey) -> str:
    return _FLAG_STRINGS.get(current_lang(), _FLAG_STRINGS["en"])[flag]


def ui(key: UIKey) -> str:
    entries = _UI_STRINGS.get(current_lang(), _UI_STRINGS["en"])
    return entries.get(key) or _UI_STRINGS["en"][key]