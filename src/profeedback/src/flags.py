class FlagRenderer:
    __slots__ = ("_cache",)

    def __init__(self) -> None:
        self._cache: TTLCache[str] = TTLCache(ttl=30.0)

    def get(self, card: Card) -> str:
        cached = self._cache.get(card.id)
        if cached is not None:
            return cached
        result = self._compute(card)
        self._cache.put(card.id, result)
        return result

    @staticmethod
    def _compute(card: Card) -> str:
        try:
            if card.type == 0:
                return FlagRenderer._build("new_card")
            history = fetch_review_history(card.id, 3)
            if len(history) >= 3 and all(ease == 1 for ease in history):
                return FlagRenderer._build("hard_exercise")
            if any(ease == 1 for ease in history[:2]):
                return FlagRenderer._build("previous_mistake")
        except Exception as error:
            log_error("FlagRenderer._compute", error)
        return ""

    @staticmethod
    def _build(flag: FlagKey) -> str:
        icon = SVG_ICONS.get(flag, "")
        label = flag_label(flag)
        css_class = flag.replace("_", "-")
        return (
            f'<div class="vfx-flag {css_class}" title="{label}">'
            f'{icon}<span>{label}</span></div>'
        )

    def clear(self) -> None:
        self._cache.clear()
